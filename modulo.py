#Função do  quadrilatero
def calcular_quadrilatero(b,h):
    a = b* h
    return a
# Função do  circulo
def calcular_circulo(r):
    a = 3.14*r**2
    return a
# Função do  Triagulo
def calcular_triagulo(b,h):
    a = (b*h)/2
    return a 
# Função do  Trapezio
def calcular_trapezio(b_menor, b_maior,h):
    a = ((b_menor+b_maior)*h)/2
    return a
